package lesson4;

public class TestMathStaticMethods {
    public static void main(String[] args) {
        System.out.println("Random " + Math.random());
        System.out.println("Square root " + Math.sqrt(9.00));
        System.out.println("Round " + Math.round(Math.random()));
        System.out.println("Absolute " + Math.abs(9));
    }
}
